void AudioSync::updateCardMatchStyle(size_t clipIdx)
{
    if (clipIdx >= m_clips.size()) return;
    const auto& _tc = Theme::colors();
    const int matchState = m_clips[clipIdx].matchState;
    const auto _radM = QString::number(Theme::metrics().radiusMd);

    // Resolve colors from matchState
    QString bgColor, borderColor, hoverBg, hoverBorder;
    switch (matchState) {
    case 2:
        bgColor     = Theme::hex(_tc.successBg);
        borderColor = Theme::hex(_tc.success);
        hoverBg     = Theme::hex(_tc.successBg.lighter(120));
        hoverBorder = Theme::hex(_tc.success.lighter(120));
        break;
    case 1:
        bgColor     = Theme::hex(_tc.warningBg);
        borderColor = Theme::hex(_tc.warning);
        hoverBg     = Theme::hex(_tc.warningBg.lighter(120));
        hoverBorder = Theme::hex(_tc.warning.lighter(120));
        break;
    default:
        bgColor     = Theme::hex(_tc.surface2);
        borderColor = Theme::hex(_tc.borderLight);
        hoverBg     = Theme::hex(_tc.surface3);
        hoverBorder = Theme::hex(_tc.border);
        break;
    }

    // Find right-pane card for this clip
    for (size_t i = 0; i < m_cardClipIndices.size(); ++i) {
        if (m_cardClipIndices[i] == static_cast<int>(clipIdx) && i < m_cardWidgets.size()) {
            auto* frame = qobject_cast<QFrame*>(m_cardWidgets[i]);
            if (frame) {
                // Determine script line number from object name
                int lineNum = 0;
                if (i < m_cardScriptLineNums.size())
                    lineNum = m_cardScriptLineNums[i];
                frame->setStyleSheet(
                    QString("QFrame#scriptCard_%3 { background: %1; border: 1px solid %2; "
                            "border-radius: %6px; margin-bottom: 2px; }"
                            "QFrame#scriptCard_%3:hover { background: %4; border: 1px solid %5; "
                            "border-radius: %6px; margin-bottom: 2px; }")
                        .arg(bgColor, borderColor).arg(lineNum)
                        .arg(hoverBg, hoverBorder).arg(_radM));
            }
            // Update badge color inside card
            auto badges = m_cardWidgets[i]->findChildren<QLabel*>();
            for (auto* lbl : badges) {
                if (lbl->text().startsWith("#")) {
                    QString badgeColor = (matchState == 2) ? Theme::hex(_tc.successBtnBg)
                                       : (matchState == 1) ? Theme::hex(_tc.warning)
                                                           : Theme::hex(_tc.surface3);
                    lbl->setStyleSheet(
                        QString("QLabel { color: %2; font-weight: bold; font-size: 12px; "
                                "background: %1; border-radius: %3px; border: none; }")
                            .arg(badgeColor, Theme::hex(_tc.textBright),
                                 QString::number(Theme::metrics().radiusSm)));
                    break;
                }
            }
            // Update status icon
            auto statusLabels = m_cardWidgets[i]->findChildren<QLabel*>();
            for (auto* lbl : statusLabels) {
                QString txt = lbl->text();
                if (txt == QStringLiteral("\u2713") || txt == "?" || txt == QStringLiteral("\u25CB")) {
                    switch (matchState) {
                    case 2:
                        lbl->setText(QStringLiteral("\u2713"));
                        lbl->setStyleSheet(QString("QLabel { color: %1; font-weight: bold; font-size: 15px; border: none; }").arg(Theme::hex(_tc.success)));
                        break;
                    case 1:
                        lbl->setText("?");
                        lbl->setStyleSheet(QString("QLabel { color: %1; font-weight: bold; font-size: 15px; border: none; }").arg(Theme::hex(_tc.warning)));
                        break;
                    default:
                        lbl->setText(QStringLiteral("\u25CB"));
                        lbl->setStyleSheet(QString("QLabel { color: %1; font-size: 15px; border: none; }").arg(Theme::hex(_tc.textDisabled)));
                        break;
                    }
                    break;
                }
            }

            // In-place swap of CONFIRM button â†” CONFIRMED label.
            // Find the right-panel widget (fixedWidth=130, third child of card outer layout).
            QVBoxLayout* rLay = nullptr;
            if (auto* outerLay = m_cardWidgets[i]->layout()) {
                for (int c = outerLay->count() - 1; c >= 0; --c) {
                    if (auto* w = outerLay->itemAt(c)->widget()) {
                        if (w->maximumWidth() == 130) {
                            rLay = qobject_cast<QVBoxLayout*>(w->layout());
                            break;
                        }
                    }
                }
            }
            if (rLay) {
                // Find the old confirm/confirmed widget at layout index 1
                QWidget* oldWidget = nullptr;
                bool isCurrentlyConfirmed = false;
                if (auto* item = rLay->itemAt(1)) {
                    oldWidget = item->widget();
                    if (auto* lbl = qobject_cast<QLabel*>(oldWidget)) {
                        if (lbl->text().contains("CONFIRMED")) isCurrentlyConfirmed = true;
                    }
                }
                bool needsSwap = (matchState == 2 && !isCurrentlyConfirmed) ||
                                 (matchState != 2 && isCurrentlyConfirmed);
                if (needsSwap && oldWidget) {
                    rLay->removeWidget(oldWidget);
                    oldWidget->deleteLater();
                    const auto _rad = QString::number(Theme::metrics().radiusSm);
                    if (matchState == 2) {
                        auto* confirmedLabel = new QLabel(QStringLiteral("\u2713  CONFIRMED"));
                        confirmedLabel->setFixedHeight(36);
                        confirmedLabel->setAlignment(Qt::AlignCenter);
                        confirmedLabel->setStyleSheet(
                            QString("QLabel { background: %1; color: %2; font-weight: bold; "
                            "font-size: 13px; border: 2px solid %3; border-radius: %4px; }")
                                .arg(Theme::hex(_tc.success), Theme::hex(_tc.textBright),
                                     Theme::hex(_tc.success), _radM));
                        rLay->insertWidget(1, confirmedLabel);
                    } else {
                        size_t ci = clipIdx;
                        auto* confirmBtn = new QPushButton(QStringLiteral("\u2713  CONFIRM"));
                        confirmBtn->setFixedHeight(36);
                        confirmBtn->setToolTip("Confirm match");
                        confirmBtn->setCursor(Qt::PointingHandCursor);
                        confirmBtn->setStyleSheet(
                            QString("QPushButton { background: %1; color: %2; font-weight: bold; "
                            "font-size: 13px; border: 2px solid %3; border-radius: %4px; }"
                            "QPushButton:hover { background: %5; }").arg(
                                Theme::hex(_tc.successBtnBg), Theme::hex(_tc.textBright),
                                Theme::hex(_tc.success), _radM, Theme::hex(_tc.successBtnHover)));
                        connect(confirmBtn, &QPushButton::clicked, this, [this, ci]() {
                            if (ci < m_clips.size()) {
                                m_clips[ci].matchState = 2;
                                populateLeftList();
                                updateCardMatchStyle(ci);
                                updateSmartBar();
                            }
                        });
                        rLay->insertWidget(1, confirmBtn);
                    }
                }
            }

            break;
        }
    }
}

void AudioSync::scrollToCard(int scriptLineNumber)
{
    if (!m_rightScrollArea || m_cardWidgets.empty()) return;

    // Remove highlight from previously highlighted card
    if (m_highlightedCard) {
        m_highlightedCard->setGraphicsEffect(nullptr);
        m_highlightedCard = nullptr;
    }

    // Find the card for this script line
    auto widgets = m_cardWidgets;  // local copy — protect against vector modification
    for (size_t i = 0; i < widgets.size(); ++i) {
        if (i < m_cardScriptLineNums.size() &&
            m_cardScriptLineNums[i] == scriptLineNumber)
        {
            auto widget = widgets[i];
            QWidget* rawWidget = widget;
            if (rawWidget) {
                // Apply highlight: accent glow that preserves match-state colors
                m_highlightedCard = widget;

                auto* frame = qobject_cast<QFrame*>(rawWidget);
                if (frame) {
                    auto* glow = new QGraphicsDropShadowEffect(frame);
                    glow->setBlurRadius(18);
                    glow->setColor(Theme::colors().accent);
                    glow->setOffset(0, 0);
                    frame->setGraphicsEffect(glow);
                }

                // Defer centering until layout is fully processed
                QPointer<QWidget> safeWidget(widget);
                QTimer::singleShot(0, this, [this, safeWidget]() {
                    if (!safeWidget || !m_rightScrollArea) return;
                    auto* vp = m_rightScrollArea->viewport();
                    auto* sb = m_rightScrollArea->verticalScrollBar();
                    if (vp && sb) {
                        QPoint cardPos = safeWidget->mapTo(m_rightScrollContent, QPoint(0, 0));
                        int cardCenter = cardPos.y() + safeWidget->height() / 2;
                        int viewportH = vp->height();
                        int targetScroll = cardCenter - viewportH / 2;
                        targetScroll = std::clamp(targetScroll, sb->minimum(), sb->maximum());
                        sb->setValue(targetScroll);
                    }
                });
            }
            break;
        }
    }
}

void AudioSync::syncLeftListFromScroll()
{
    if (!m_rightScrollArea || !m_leftScriptList || m_cardWidgets.empty()) return;

    // Find which card is at the top of the viewport
    QPoint viewportTop = m_rightScrollArea->viewport()->mapToGlobal(QPoint(0, 0));

    int bestRow = -1;
    int bestDist = INT_MAX;
    auto widgets = m_cardWidgets;  // local copy — protect against vector modification
    for (size_t i = 0; i < widgets.size(); ++i) {
        auto w = widgets[i];
        if (!w) continue;
        QPoint cardTop = w->mapToGlobal(QPoint(0, 0));
        int dist = std::abs(cardTop.y() - viewportTop.y());
        if (dist < bestDist) {
            bestDist = dist;
            bestRow = static_cast<int>(i);
        }
    }

    if (bestRow >= 0 && bestRow < m_leftScriptList->count()) {
        m_leftScriptList->blockSignals(true);
        m_leftScriptList->setCurrentRow(bestRow);
        m_leftScriptList->blockSignals(false);
    }
}


